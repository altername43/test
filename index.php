	//сначала считаем количество прямоугольников в 1 горизонтальной строчке
	while ($b > 1) {
		$rectangles = $rectangles + ($b - 1);
		--$b;
		$j++;
	}
	//теперь полученное количество умножаем на общее количество строк
	$rectangles = $rectangles * ($a - 1); // $a - 1, потому что для удобства ведем счет с 1, а не с 0
