			/* не успела доделать до указанного времени, потому что освободилась с работы только в 7 вечера :(
			   реализация подсчета прямоугольников побольше планировалась по следующему алгоритму:
			   начинаем цикл от точки 3 с переменной в цикле, что равна 1 (т.е. начинаем с прямоугольников, которые занимают 2 строки), при каждой итерации увеличиваем
