/**
 * @param int $a
 * @param int $b
 * @return int
 */
function getRectangles(int $a, int $b): int
{
	$rectangles = 0;

	//если число меньше 2 (для удобства начинаем считать не с 0, а с 1), то прямоугольников не будет
	if ($a === 0 || $a === 1 || $b === 0 || $b === 1) {
		return $rectangles;
	}

	//доп. переменная для подсчета итераций (понадобится в следующем цикле
